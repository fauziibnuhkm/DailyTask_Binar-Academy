function checkHobby(key3 , value3) {
  return key3.hobby === value3;
}

function checkFootballHobby(y) {
  return y.hobby.find(z => checkHobby(z, "football"));
}

function footballFans(data) {
  let arr = [];
  for (let i = 0; i < data.length; i++) {
    let dataFriends = data[i].friends;
    for (let j = 0; j < dataFriends.length; j++) {
      if (checkFootballHobby(dataFriends[j])) {
        arr.push(dataFriends[j]);
      }
    }
  }
  return arr
}

